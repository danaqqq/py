a = int(input('wpisz cyfę x: '))
b = int(input('wpisz kolejną cyfrę y:'))

x = a
y = b
print('x>y', x > y)
print('x<y', x < y)

print('x=y', x == y)
print('x=', a, x == a)

print('x!=y', x != y)
print('x!=', a, x != a)

print('x>=', a, x >= a)
print('x>=', b, x >= b)

print('x<=y', x <= y)
print('x<=', b, x <= b)